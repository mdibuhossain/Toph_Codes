#include <stdio.h>
char ar[26][26];
int main()
{
	int c, r, count = 0, i, j;
	scanf("%d%d", &c, &r);
	for (i = 0; i < c; i++)
	{
		scanf("%s", &ar[i]);
	}
	for (i = 0; i < c; i++)
	{
		for (j = 0; j < r; j++)
		{
			if (ar[i][j] != '*' && ar[i][j - 1] != '*' && ar[i][j + 1] != '*' && ar[i - 1][j] != '*' && ar[i + 1][j] != '*')
			{
				count++;
			}
		}
	}
	printf("%d\n", count);
	return 0;
}