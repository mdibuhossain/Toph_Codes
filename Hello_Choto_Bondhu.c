#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
char ar[1000000000];
int main()
{
    int t;
    scanf("%d", &t);
    while (t--)
    {
        scanf("%s", &ar);
        int n = ar[strlen(ar) - 1] - '0';
        printf("%d\n", (n * n) % 10);
    }
    return 0;
}