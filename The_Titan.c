#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int n;
    long long fin = 0;
    scanf("%d", &n);
    for (int i = 1; i <= n; i++)
    {
        fin += pow(i, 2);
    }
    printf("%lld\n", fin);
    return 0;
}