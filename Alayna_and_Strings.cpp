#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int cap = 0, sm = 0;
    string st;
    cin >> st;
    for (int i = 0; i < st.size(); i++)
    {
        if (st[i] >= 65 && st[i] <= 90)
            cap++;
        else if (st[i] >= 97 && st[i] <= 122)
            sm++;
    }
    cout << cap << " " << sm << endl;
    return 0;
}