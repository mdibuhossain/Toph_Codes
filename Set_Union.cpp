#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n, m, x;
    cin >> n >> m;
    map<int, int> mp;
    while (n--)
    {
        cin >> x;
        mp[x]++;
    }
    while (m--)
    {
        cin >> x;
        mp[x]++;
    }
    for (auto &i : mp)
        cout << i.first << ' ';
    return 0;
}