#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int t;
    scanf("%d", &t);
    while (t--)
    {
        long long n, m, final;
        scanf("%lld%lld", &n, &m);
        final = (m * 2) - (n * 2);
        printf("%lld\n", final);
    }
    return 0;
}